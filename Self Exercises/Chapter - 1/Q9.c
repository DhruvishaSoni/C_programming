/* 
   Q.9  What is the purpose of the #include <stdio.h> and #include <conio.h> directives in a C program?
        Explain its significance.
*/


//  Answer:

/*
=> #include <stdio.h>: 
          Includes the Standard Input/Output library in the program, which provides functions like printf(), 
	      scanf(), etc.
	   
	   
=> #include <conio.h>: 
          Includes Console Input/Output functions, such as getch() and clrscr(), primarily used in MS-DOS 
		  systems. It's not part of the standard C library and may not be available in modern compilers.
*/
