/*
   Q.11  What are format specifiers in C? Why are they used in input/output operations?
*/


// Answer:

/*
   Format specifiers are used in C for specifying the type of data when using functions like printf() and 
   scanf(). They tell the compiler how to interpret the data during input/output operations. For example, 
   %d is used for integers, %f for floats, and %c for characters.
*/
