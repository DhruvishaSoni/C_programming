/*
   Q.3  What are the ranges of values that can be stored in int, float, and char data types?
*/


// Answer:


/*

=>  int: Typically ranges from -32,768 to 32,767 for a 2-byte (16-bit) int and from -2,147,483,648 to 2,147,
    483,647 for a 4-byte (32-bit) int.
=>  float: Can store values roughly between 1.2E-38 to 3.4E+38 (depending on the system and precision).
=>  char: Can store values from -128 to 127 (1 byte, signed char) or 0 to 255 (unsigned char).

*/
