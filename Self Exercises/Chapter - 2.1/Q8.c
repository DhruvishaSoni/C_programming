/*
   Q.8  Describe the scope and lifetime of a variable in C.
*/


// Answer:

/*

=>  Scope: Refers to the region of the program where the variable is accessible. Variables can have global scope 
    (accessible throughout the program) or local scope (accessible only within the function or block where they 
	are declared).
=>  Lifetime: Refers to the period during which the variable exists in memory. Local variables exist only during 
    the execution of the block or function where they are declared, while global variables exist for the entire 
	runtime of the program.
	
*/
