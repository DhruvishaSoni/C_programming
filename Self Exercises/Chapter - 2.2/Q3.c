/*
   Q.3  Can you take multiple inputs using a single scanf() statement? If yes, how?
*/


// Answer:

/*
   Yes, you can take multiple inputs using a single scanf() statement by providing multiple format specifiers 
   and variables.
*/

// Example:

int a;
float b;
scanf("%d %f", &a, &b);

/*
   In this case, both an integer and a float value will be read in a single statement.
*/

